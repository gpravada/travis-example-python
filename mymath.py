def operator_Add(in1, in2):
    return in1+in2

def operator_Sub(in1, in2):
    return in1-in2

def operator_Mul(in1, in2):
    return in1*in2

def operator_And(in1, in2):
    return in1&in2

def operator_Or(in1, in2):
    return in1|in2

def operator_Xor(in1, in2):
    return in1^in2

def operator_Xnor(in1, in2):
    return ~(in1^in2)












